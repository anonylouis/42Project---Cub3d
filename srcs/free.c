#include "cub3D.h"
void ft_free(void *f)
{
    if (f != NULL)
    {
        free(f);
    }
}

void ft_free_all(void **f)
{
    int i = 0;
    while (f[i] != NULL)
    {
        free(f[i]);
        i++;
    }
    ft_free(f);
}